package br.com.rnp.cif;

public class CifObject {
	
	private String address;
	private String assessment;
	private String severity;
	private String impact;
	private String description;
	private String portlist;
	private String protocol;
	private String confidence;
	private String guid;
	private String restriction;
	private String alternativeid;
	private String alternativeid_restriction;

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getAssessment() {
        return assessment;
    }

    public void setAssessment(String assessment) {
        this.assessment = assessment;
    }

    public String getSeverity() {
        return severity;
    }

    public void setSeverity(String severity) {
        this.severity = severity;
    }

    public String getImpact() {
        return impact;
    }

    public void setImpact(String impact) {
        this.impact = impact;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getPortlist() {
        return portlist;
    }

    public void setPortlist(String portlist) {
        this.portlist = portlist;
    }

    public String getProtocol() {
        return protocol;
    }

    public void setProtocol(String protocol) {
        this.protocol = protocol;
    }

    public String getConfidence() {
        return confidence;
    }

    public void setConfidence(String confidence) {
        this.confidence = confidence;
    }

    public String getGuid() {
        return guid;
    }

    public void setGuid(String guid) {
        this.guid = guid;
    }

    public String getRestriction() {
        return restriction;
    }

    public void setRestriction(String restriction) {
        this.restriction = restriction;
    }

    public String getAlternativeid() {
        return alternativeid;
    }

    public void setAlternativeid(String alternativeid) {
        this.alternativeid = alternativeid;
    }

    public String getAlternativeid_restriction() {
        return alternativeid_restriction;
    }

    public void setAlternativeid_restriction(String alternativeid_restriction) {
        this.alternativeid_restriction = alternativeid_restriction;
    }
        
        
        
	
	
	
	

}
